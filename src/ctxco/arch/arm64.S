/* make context (refer to boost.context)
 *
 *
 *             --------------------------------------------------------------------------------------
 * stackdata: |                                                       |         context             ||
 *             --------------------------------------------------------------------------------------
 *                                                                                              (16-align)
 *
 *             ---------------------------------------------------------------------
 * context:   |   x19   |   x20   |   x21   |   x22   |   x23   |   x24   |   x25   |
 *             ---------------------------------------------------------------------
 *            0         8         16        24        32        40        48
 *
 *
 *                                                     __end     func
 *             ---------------------------------------------------------------------
 *            |   x26   |   x27   |   x28   |   fp    |   lr    |   pc    | padding |
 *             ---------------------------------------------------------------------
 *            56        64        72        80        88        96        |
 *                                                                        |
 *                                                                    16-align
 *
 *
 * @param stackdata     the stack data (x0)
 * @param stacksize     the stack size (x1)
 * @param func          the entry function (x2)
 *
 * @return              the context pointer (x0)
 */
.text
.align  2
.global getctx
.type   getctx, %function
getctx:
    // save the stack top to x0
    add x0, x0, x1
    // 16-align of the stack top address
    and x0, x0, ~0xf
    sub x0, x0, #112
    // context.pc = func
    str x2, [x0, #96]
    // get the address of label __end
    adr x1, __end
    // context.lr = the address of label __end
    str x1, [x0, #88]
    // return pointer to context-data (x0)
    ret x30
__end:
    // exit(0)
    mov x0, #0
    bl _exit
.size getctx,.-getctx


/* jump context (refer to boost.context)
 *
 * @param context       the to-context (x0)
 * @param priv          the passed user private data (x1)
 *
 * @return              the from-context (context: x0, priv: x1)
 */
.text
.align  2
.global setctx
.type   setctx, %function
setctx:
    sub sp, sp, #0x70
    // save x19 - x30
    stp x19, x20, [sp, #0x00]
    stp x21, x22, [sp, #0x10]
    stp x23, x24, [sp, #0x20]
    stp x25, x26, [sp, #0x30]
    stp x27, x28, [sp, #0x40]
    stp x29, x30, [sp, #0x50]
    // save lr as pc
    str x30, [sp, #0x60]
    // save the old context(sp) to x4
    mov x4, sp
    // switch to the new context(sp) and stack
    mov sp, x0
    // restore x19 - x30
    ldp x19, x20, [sp, #0x00]
    ldp x21, x22, [sp, #0x10]
    ldp x23, x24, [sp, #0x20]
    ldp x25, x26, [sp, #0x30]
    ldp x27, x28, [sp, #0x40]
    ldp x29, x30, [sp, #0x50]
    // pass old-context(context: x0, priv: x1) arguments to the context function and return from-context: retval(context: x0, priv: x1) from jump
    mov x0, x4
    // load pc
    ldr x4, [sp, #0x60]
    // restore stack space
    add sp, sp, #0x70
    // jump to the return or entry address(pc)
    ret x4
.size setctx,.-setctx